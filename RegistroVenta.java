        private void actualizarTotal() {
    double total = 0;
    for (int i = 0; i < modeloTicket.getRowCount(); i++) {
        total += (double) modeloTicket.getValueAt(i, 3); // columna Subtotal
    }
    lblTotal.setText("$" + total);
}
        
        
        private double obtenerPrecioProducto(String nombreProducto) {
    double precio = 0;
    try {
        Connection con = new Conector().conectaMySQL();
        String sql = "SELECT dp.Precio FROM detalleproducto dp " +
                     "JOIN productos p ON dp.id_Producto = p.id_Producto " +
                     "WHERE p.Nombre = ?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, nombreProducto);
        ResultSet rs = ps.executeQuery();
        if (rs.next()) {
            precio = rs.getDouble("Precio");
        }
    } catch (SQLException e) {
        System.out.println("Error al obtener precio: " + e.getMessage());
    }
    return precio;
}

        
        
public void guardarVentasEnBD(int totalVenta) {
    Conector obj = new Conector();
    Connection con = obj.conectaMySQL();
    String sql = "INSERT INTO ventas (FechaVenta, TotalVenta) VALUES (CURDATE(), ?)";

    try (PreparedStatement ps = con.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS)) {
        ps.setInt(1, totalVenta);
        ps.executeUpdate();

        ResultSet rs = ps.getGeneratedKeys();
        if (rs.next()) {
            int folio = rs.getInt(1);
            lblFolio.setText("Folio: " + folio);
        }

        JOptionPane.showMessageDialog(this, "Venta registrada exitosamente.");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al registrar venta: " + e.getMessage());
    }
}





