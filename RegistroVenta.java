    private void btnAgregarAlTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarAlTicketActionPerformed
    // 1. Tomamos el producto del combo
    String producto = jComboBoxProducto.getSelectedItem().toString();

    // 2. Tomamos la cantidad desde el campo de texto
    int cantidad = Integer.parseInt(txtCantidad.getText());

    // 3. Buscamos el precio desde la base de datos
    double precio = obtenerPrecioProducto(producto);

    // 4. Calculamos subtotal
    double subtotal = cantidad * precio;

    // 5. Agregamos la fila a la tabla
    modeloTicket.addRow(new Object[]{producto, cantidad, precio, subtotal});

    // 6. Actualizamos el total
    actualizarTotal();        // TODO add your handling code here:
    }//GEN-LAST:event_btnAgregarAlTicketActionPerformed

